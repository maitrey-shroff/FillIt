#include "../inc/fillit.h"

int		ft_sqrt(int nb)
{
	int	i;

	i = 0;
	if (nb > 46340 * 46340)
		return (0);
	while (i < nb)
	{
		if (((double)nb / (i * i)) <= 1)
			return (i);
		i++;
	}
	return (0);
}
